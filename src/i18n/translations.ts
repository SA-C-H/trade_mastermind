export type AiModeId = 'coach' | 'analysis' | 'sentiment';

export function getAiQuickPrompts(locale: Locale, mode: AiModeId): string[] {
  const prompts = (translations[locale].ai as { prompts: Record<AiModeId, string[]> }).prompts;
  const fallback = (translations.en.ai as { prompts: Record<AiModeId, string[]> }).prompts;
  return prompts[mode] ?? fallback[mode];
}

function getNested(obj: unknown, path: string[]): string | undefined {
  let cur: unknown = obj;
  for (const p of path) {
    if (cur === null || cur === undefined || typeof cur !== 'object') return undefined;
    cur = (cur as Record<string, unknown>)[p];
  }
  return typeof cur === 'string' ? cur : undefined;
}

export function translate(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  const parts = key.split('.');
  const raw = getNested(translations[locale], parts) ?? getNested(translations.en, parts) ?? key;
  let out = raw;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      out = out.replaceAll(`{{${k}}}`, String(v));
    });
  }
  return out;
}
